void checkresendcmd(void)
{   
   //------------ loop checking to send C1 ------------- 
   
   if(iflashstep==1)   
   {
      if(iwaitrespcount>=WAITREPEATTIME)
      {
         iwaitrespcount=0;
         flg_repeat=1;
         flg_sendtype=1;            
      }         
   }
   
   if (flg_sendtype==1)       
   {        
      flg_sendtype=0;
      iflashstep=1;
      iwaitrespcount=0;      
      
      mastercmd[0]=CMDHEADER1;  //0x55;
      mastercmd[1]=CMDHEADER2;  //0xAA;
      mastercmd[2]=0x03;    
      mastercmd[3]=0xC1;        //inform to send 2k flash data
      mastercmd[4]=islaveaddrout;
      mastercmd[5]=get_checksum_8(mastercmd,5);
      uartsendcmd(mastercmd,6);
      
      flg_repeat=0;         
   }
   
   
   //------------ loop checking to  send C2 ------------- 
   
   if(iflashstep==3)   
   {
      if(iwaitrespcount>=WAITREPEATTIME)
      {
         iwaitrespcount=0;
         flg_repeat=1;
         flg_sendtype=2;   
      }         
   }      

   if (flg_sendtype==2)       
   {        
      flg_sendtype=0;
      iflashstep=3;
      iwaitrespcount=0;
      
      static uint32_t isendnum=0;
      

      if(flg_repeat==1)
      {
         idataoff-=isendnum;
         irestlen+=isendnum;            
      }

      isendnum=PACKAGELEN;
      if(irestlen<=PACKAGELEN)
      {
         isendnum=irestlen;
         flg_lastpackage=1;
      }
             

      mastercmd[0]=CMDHEADER1;  //0x55;
      mastercmd[1]=CMDHEADER2;  //0xAA;
      mastercmd[2]=isendnum+3;  //len 
      mastercmd[3]=0xC2;      
      mastercmd[4]=islaveaddrout;      

      for(uint8_t i=0; i<isendnum; i++)  //64         
         mastercmd[5+i] = FLASHTEMPDATA[idataoff+i];

      mastercmd[isendnum+5]=get_checksum_8(mastercmd,isendnum+5); 
      uartsendcmd(mastercmd,isendnum+6);   


      idataoff+=isendnum;
      irestlen-=isendnum;

      flg_repeat=0;         
   }
   
   //------------ loop checking to send C3 ------------- 
   
   if(iflashstep==5)   
   {
      if(iwaitrespcount>=WAITREPEATTIME)
      {
         iwaitrespcount=0;
         flg_repeat=1;
         flg_sendtype=3;   
                     
      }         
   }        
   
   if (flg_sendtype==3)       
   {        
      flg_sendtype=0;
      iflashstep=5;
      iwaitrespcount=0;
      uint32_t icrc=0;
      uint32_t icrclen=0;
      
      //make crc 
      icrclen=byte2uint32( (uint8_t *)FLASHTEMPDATA,icrctempdata,idataoff);   
      icrc = calcu_crc32(icrctempdata, icrclen);  
      
      mastercmd[0]=CMDHEADER1;  //0x55;
      mastercmd[1]=CMDHEADER2;  //0xAA;
      mastercmd[2]=0x07;    
      mastercmd[3]=0xC3;  
      mastercmd[4]=islaveaddrout;  

      mastercmd[5]=icrc>>24;  
      mastercmd[6]=icrc>>16;   
      mastercmd[7]=icrc>>8;   
      mastercmd[8]=icrc;  

      mastercmd[9]=get_checksum_8(mastercmd,9); 
      uartsendcmd(mastercmd,10);
      
   }     
}      


